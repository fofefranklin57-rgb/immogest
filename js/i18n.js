// ────────────────────────────────────────────────────────────────
//  FONCTION PRINCIPALE
// ────────────────────────────────────────────────────────────────
function t(key) {
  if (key === null || key === undefined) return '';
  var k = String(key);
  if (LANG === 'fr') return k;
  var block = TR[LANG];
  if (block) {
    var val = block[k];
    if (val !== undefined) return val;
  }
  // Fallback vers EN si clé absente dans la langue courante
  if (LANG !== 'en' && TR.en) {
    var valEn = TR.en[k];
    if (valEn !== undefined) return valEn;
  }
  return k; // fallback final : texte français
}

// ── Appliquer les traductions aux éléments data-i18n ────────────
function applyStaticI18n() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
}

// ── Ouvrir / fermer le menu langue ──────────────────────────────
function toggleLangMenu() {
  var menu = document.getElementById('lang-menu');
  if (!menu) return;
  var isOpen = menu.style.display !== 'none';
  menu.style.display = isOpen ? 'none' : 'block';
  if (!isOpen) {
    // Fermer en cliquant ailleurs
    setTimeout(function() {
      document.addEventListener('click', function _close(e) {
        var sel = document.getElementById('lang-selector');
        if (sel && !sel.contains(e.target)) {
          menu.style.display = 'none';
          document.removeEventListener('click', _close);
        }
      });
    }, 10);
  }
}

// ── Cycle à travers les 5 langues (fallback bouton simple) ───────
function cycleLang() {
  var idx = LANGS_ORDER.indexOf(LANG);
  setLang(LANGS_ORDER[(idx + 1) % LANGS_ORDER.length]);
}

// ── Changer la langue ────────────────────────────────────────────
function setLang(lang) {
  if (!LANGS_ORDER.includes(lang)) return;
  LANG = lang;
  localStorage.setItem('immogest_lang', lang);
  // RTL pour arabe
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  applyStaticI18n();
  // Mettre à jour le code langue affiché
  var codeEl = document.getElementById('lang-code');
  if (codeEl) codeEl.textContent = lang.toUpperCase();
  // Mettre à jour le bouton thème
  var themeBtn = document.getElementById('btn-theme-toggle');
  if (themeBtn) {
    var isDark = document.body.classList.contains('dark');
    themeBtn.textContent = isDark ? t('☀️ Mode clair') : t('🌙 Mode sombre');
  }
  // Re-render la page courante
  if (typeof renderCurrent === 'function') renderCurrent();
}
